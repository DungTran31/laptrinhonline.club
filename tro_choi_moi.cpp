int countOnes(vector<int> a) {
    int cnt = 0;
    for (int i = 0; i < a.size(); ++i) {
        if (a[i] == 1) {
            cnt++;
        }
    }
    return cnt;
}

void sol() {
    int n;
    cin >> n;
    vector<int> a(n), b(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
        b[i] = a[i];
    }

    int maxOnes = 0;
    for (int i = 0; i < n; ++i) {
        int cnt = 0;
        for (int j = i; j < n; ++j) {
            if (a[j] == 1) {
                a[j] = 0;
            } else {
                a[j] = 1;
            }
            cnt = countOnes(a);
            maxOnes = max(maxOnes, cnt);
        }
        a = b;
    }

    cout << maxOnes << endl;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

