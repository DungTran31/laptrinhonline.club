void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

struct node {
    int elem, num;
    node *L, *R;
    node(int x) {
        elem = x;
        num = 1;
        L = R = nullptr;
    }
};

void update(node *&H, int x) {
    if (!H)
        H = new node(x);
    else if (H->elem == x)
        H->num++;
    else
        update((x <= H->elem) ? H->L : H->R, x);
}

void get(node *H, int x, node *&p) {
    if (!H)
        return;
    if (x <= H->elem)
        return get(H->L, x, p);
    get(H->R, x, p);
    if (!p)
        p = H;
}

void sol() {
    int n, x; cin >> n;
    node *H = nullptr;
    for (int i = 1; i <= n; i++) {
        cin >> x;
        update(H, x);
        node *p = nullptr;
        get(H, x, p);
        if (!p) cout << 0 << endl;
        else cout << p->elem << " " << p->num << endl;
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

