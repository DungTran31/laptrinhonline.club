void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

void sol() {
    string str; cin >> str;
    vector<int> a;

    for(int i = 0; i < str.length(); i++) {
        if(isdigit(str[i])) {
            a.push_back(str[i] - '0');
        }
    }
    sort(a.begin(), a.end());
    for(int i = 0; i < a.size() - 1; i++) {
        cout << a[i] << "+";
    }
    cout << a[a.size() - 1];
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

