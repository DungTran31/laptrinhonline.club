void sol() {
    int n; cin >> n;
    char a[n];
    vector<char> b;
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
        if (a[i] == a[i-1])
            continue;
        else b.push_back(a[i]);
    }
    for (int i = 0; i < b.size(); i++){
        cout << b[i];
    }
    
}

