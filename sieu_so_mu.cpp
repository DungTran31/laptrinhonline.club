int pow_mod(int n){
    if(n == 0) return 1;
    if(n == 1) return 2;
    int tmp = pow_mod(n / 2) % MOD;
    int res = tmp * tmp % MOD;
    if(n % 2 == 1) res = res * 2 % MOD;
    return res;
}

