void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

void sol() {
    string x;
    cin >> x;

    int n = x.size();
    int q;
    cin >> q;

    char convert_char[26];
    for (char i = 'a'; i <= 'z'; ++i) {
        convert_char[i-'a'] = i;
    }

    while (q--) {
        char c;
        cin >> c;
        if (c == 'R') {
            char a, b;
            cin >> a >> b;
            for(int i = 0; i < 26; i++){
                if(convert_char[i] == a)
                    convert_char[i] = b;
            }
        } else if (c == 'D') {
            char a;
            cin >> a;
            for(int i = 0; i < 26; i++){
                if(convert_char[i] == a)
                    convert_char[i] = '#';
            }
        }
    }

    for (int i = 0; i < n; i++) {
        if (convert_char[x[i]-'a'] != '#') {
            cout << convert_char[x[i]-'a'];
        }
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

