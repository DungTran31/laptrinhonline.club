void sol() {
    int n, m, k;
    cin >> n >> m >> k;
    int cards_per_player = n / k;
    if(m <= cards_per_player) { cout << m;}
    else {
        cout << cards_per_player - ((m - cards_per_player) + (k - 2)) / (k - 1);
    }



}

