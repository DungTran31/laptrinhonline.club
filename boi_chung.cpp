// Hàm tính ước số chung lớn nhất (GCD)
int gcd(int a, int b) {
    if (b == 0) {
        return a;
    } else {
        return gcd(b, a % b);
    }
}

// Hàm tính bội chung nhỏ nhất (LCM)
int lcm(int a, int b) {
    return (a * b) / gcd(a, b);
}


