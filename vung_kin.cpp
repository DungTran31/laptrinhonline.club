void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

int a[205][205], n, m;
int hh[] = {-1, 1, 0, 0};
int hc[] = {0, 0, -1, 1};

void dfs(int u, int v) {
    stack<pair<int, int>> S;
    a[u][v] = 1; // đã xét 1 th
    S.push({u, v});
    

    while (S.size() > 0) {
        int x = S.top().first, y = S.top().second;
        S.pop();
        for(int h = 0; h < 4; h++) {
            int i = hh[h], j = hc[h];
            if(1 <= x+i && x+i <=n && 1<=y+j && y+j<=m && a[x+i][y+j] == 0) {
                a[x+i][y+j] = 1;
                S.push({x+i,y+j});
            }
        }
    }    
}

void sol() {
    cin >> n >> m;
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            cin >> a[i][j];
        }
    }
    int k = 0;
    for (int i = 1; i <= n; i++) {
        if (a[i][1] == 0) dfs(i, 1);
        if (a[i][m] == 0) dfs(i, m);
    }

    for (int i = 1; i <= m; i++) {
        if (a[1][i] == 0) dfs(1, i);
        if (a[n][i] == 0) dfs(n, i);
    }

    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            if (a[i][j] == 0) {
                k++;
                dfs(i, j);
            }
        }
    }
    cout << k;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

