using namespace std;
#ifndef ONLINE_JUDGE
clock_t tStart = clock();
#endif

void runtime()
{
#ifndef ONLINE_JUDGE
    fprintf(stderr, ">> Runtime: %.10fs\n", (double)(clock() - tStart) / CLOCKS_PER_SEC);
#endif
}

void sol(){
    long n, k, L, R, a[100005], b[100005], m = 0;
    cin >> n;
    for (k = 2; k * k <= 2 * n; k++){
        if (2 * n % k == 0){
            L = 2 * n / k + 1 - k;
            if (L % 2 == 0){
                L /= 2;
                R = L + k - 1;
                m++;
                a[m] = L;
                b[m] = R;
            }
        }
    }
    cout << m << endl;
    for (int i = 0; i < m; i++)
        cout << a[i + 1] << " " << b[i + 1] << endl;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

