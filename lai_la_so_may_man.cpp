using namespace std;
#ifndef ONLINE_JUDGE
    clock_t tStart = clock();
#endif

void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}


int n, res = LLONG_MAX; 

void Try(int SMM) {
    if (SMM >= n) { 
        int cnt = 0;
        int tmp = SMM;
        while (tmp) {
            if (tmp % 10 == 4)
                cnt++;
            else
                cnt--;
            tmp /= 10;
        }
        if (cnt == 0) {
            res = min(res, SMM); // Cập nhật giá trị nhỏ nhất cho res
            return;
        }
    }
    if (SMM > LLONG_MAX / 10) // Kiểm tra tràn số
        return;
    Try(SMM * 10 + 4);
    Try(SMM * 10 + 7);
}


void sol() {
    cin >> n;
    Try(0);
    if (res == LLONG_MAX)
        cout << -1; // Nếu không tìm thấy số siêu may mắn
    else
        cout << res;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

