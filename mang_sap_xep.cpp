// Hàm kiểm tra mảng đã được sắp xếp tăng hay giảm chưa
string checkSorted(vector<int> a, int n) {
    bool increasing = true;
    bool decreasing = true;

    for (int i = 1; i < n; ++i) {
        if (a[i] < a[i - 1]) {
            increasing = false;
            break;
        }
    }

    for (int i = 1; i < n; ++i) {
        if (a[i] > a[i - 1]) {
            decreasing = false;
            break;
        }
    }

    if (increasing || decreasing) {
        return "YES";
    } else {
        return "NO";
    }
}

void sol() {
    int n; cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    cout << checkSorted(a, n);
}

