int power(int base, int exp, int mod) {
    int result = 1;
    base %= mod;
    while (exp > 0) {
        if (exp & 1) {
            result = (result * base) % mod;
        }
        exp >>= 1;
        base = (base * base) % mod;
    }
    return result;
}

void sol() {
    int k;
    cin >> k;

    vector<bool> isPrime(k + 1, true);
    vector<int> primes;
    for (int i = 2; i <= k; ++i) {
        if (isPrime[i]) {
            primes.push_back(i);
            for (int j = i * i; j <= k; j += i) {
                isPrime[j] = false;
            }
        }
    }

    int result = 1;
    for (int prime : primes) {
        result = (result * prime) % MOD;
    }

    cout << result % MOD << endl;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

