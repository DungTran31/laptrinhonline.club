void sol() {
    int n;
    cin >> n;

    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }

    int res = 0;

    for (int i = 0; i < n; ++i) {
        int minVal = a[i];
        int maxVal = a[i];
        for (int j = i; j < n; ++j) {
            minVal = min(minVal, a[j]);
            maxVal = max(maxVal, a[j]);
            res += maxVal - minVal;
        }
    }

    cout << res << endl;
}

