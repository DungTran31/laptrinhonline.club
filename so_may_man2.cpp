void bfs(int n) {
    queue<int> q;
    q.push(0);

    while (true) {
        int current = q.front();
        q.pop();

        if (current * 10 > 999999999) {
            return;
        }

        if (current != 0) {
            int multipleOfTen = current * 10;
            if (multipleOfTen % n == 0) {
                cout << multipleOfTen << endl;
            }
            q.push(multipleOfTen);
        }

        for (int digit : {1, 2, 5}) {
            int newNumber = current * 10 + digit;
            if (newNumber % n == 0) {
                cout << newNumber << endl;
            }
            q.push(newNumber);
        }
    }
}

