map<int, int> Dic;
int zero(int n) {
    if(Dic.find(n) != Dic.end()) return Dic[n];
    if(n == 0) return Dic[n]=1;
    int s = 0;
    for(int a = 1; a * a <= n; a++) 
    if(n % a == 0) s = (s + zero((a - 1) * (n / a + 1))) % MOD;
    return Dic[n] = s;

}

