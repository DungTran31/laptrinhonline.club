void bfs(int n) {
    queue<int> q;
    q.push(0);

    for (int i = 0; i < 1e5; ++i) {
        int u = q.front();
        q.pop();

        if ((u * 10 + 6) % n == 0) {
            cout << u * 10 + 6;
            return;
        } else {
            q.push(u * 10 + 6);
        }

        if ((u * 10 + 8) % n == 0) {
            cout << u * 10 + 8;
            return;
        } else {
            q.push(u * 10 + 8);
        }
    }
}

void sol() {
    int n;
    cin >> n;

    if (n % 5 == 0) {
        cout << 0;
    } else {
        bfs(n);
    }
}

