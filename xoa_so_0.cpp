void sol() {
    string s; cin >> s;
    bool check = false;
    int cnt0 = 0, start = 0, res = 0; 
    for (int i = 0; i < s.length(); i++){
        if(s[i] == '1') {
            start = i;
            break;
        }
    }
    for (int i = start + 1; i < s.length(); i++) {
        if(s[i] == '0') {
            cnt0++;
            check = true;
        }
        if(s[i] == '1' && check) {
            res += cnt0;
            cnt0 = 0;
            check = false;
        }
    }
    cout << res;
}

