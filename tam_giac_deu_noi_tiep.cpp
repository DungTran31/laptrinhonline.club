void sol() {
    int n;
    while(cin >> n) {
        int x[n+1]={0};
        map<int,int>M;
        for(int i = 1; i <= n; i++) { 
            cin >> x[i];
            x[i] += x[i - 1];
            M[x[i]]++;
        }
        for(int i=1;i<=n;i++) {
            M[x[i]+x[n]]++;
        }
        if(x[n] % 3) cout << 0 << endl;
        else {
            int d = x[n] / 3;
            int res = 0;
            for(int i = 0; x[i] < d; i++) 
                if(M.find(x[i] + d) !=M.end() && M.find(x[i] + 2 * d) != M.end()) 
                    res++;
            cout << res << endl;
        }
    }
}

