void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

ll prime[1000001];

//O(nloglogn)
void sieve(){
    // Coi tất cả các số từ 0 cho tới n là số nguyên tố
    // prime[i] = 1 là số ngto
    // prime[i] = 0 ko là số ngto
    for(ll i = 0; i<=1000000;i++){
        prime[i]= 1;
    }
    //Loại 0 và 1 thủ công
    prime[0] = prime[1] = 0;
    for(ll i = 2; i<=1000; i++){// i<=1000 ý là căn bậc 2 của n là 1000000
        //Nếu i là số nguyên tố
        if(prime[i]){
            //Duyệt tất cả các bội của i và cho nó ko là số ngto
            for(ll j = i*i; j<=1000000;j+=i){
                prime[j] = 0; // j ko còn là số ngto nữa
            }
        }
    }
}

// Hàm đếm số cách phân tích n thành tổng của ba số nguyên tố
int solve(int n) {
    int count = 0;
    for (int i = 2; i <= n / 3; ++i) {
        if (prime[i]) {
            for (int j = i; j <= (n - i) / 2; ++j) {
                if (prime[j] && prime[n - i - j]) {
                    count++;
                }
            }
        }
    }
    return count;
}

void sol() {
    sieve();
    int n; cin >> n;
    int res = solve(n);
    cout << res;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

