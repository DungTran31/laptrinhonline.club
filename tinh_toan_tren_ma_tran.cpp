void sol() {
    int n, m; cin >> n >> m;
    int i, j, a[n][m];
    for (i = 0; i < n; i++){
        for (j = 0; j < m; j++){
            cin >> a[i][j];
        }
    }
    for (i = 0; i < n; i++){
        int sum = 0;
        for (j = 0; j < m; j++){
            sum += a[i][j];
        }
        cout << sum << " ";
    }
    cout << endl;
    for (j = 0; j < m; j++) {
        int maxCol = a[0][j];
        for (i = 1; i < n; i++) {
            maxCol = max(maxCol, a[i][j]);
        }
        cout << maxCol << " ";
    }
    cout << endl;
    int count = 0;
    int sum = 0;
    for (i = 1; i < n; i += 2) {
        for (j = 0; j < m; j += 2) {
            if (a[i][j] % 3 == 0) {
                sum += a[i][j];
                count++;
            }
        }
    }
    if(count > 0) {
        double avr = (double)sum / count;
        cout << setprecision(3) << fixed << avr;
    } else {
        cout << "khong co so thoa man";
    }
}

