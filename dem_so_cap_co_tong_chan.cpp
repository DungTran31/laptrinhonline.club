void sol() {
    int n;
    cin >> n;
    int c = 0, l = 0, x = 0;

    while(n--){
        int x;
        cin >> x;
        if(x % 2 == 0) c++;
        if(x % 2 != 0) l++;
    }
    int res = c * (c-1) / 2 + l * (l - 1) / 2;
    cout << res;
}

