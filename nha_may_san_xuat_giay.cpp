/*
Nhà máy sản xuất giày bóng đá Adidas hiện tại đang có n con Robot sản xuất giày và số đôi giày cần hoàn thiện là t đôi giày. Biết các con Robot sản xuất giày đồng thời. Giám đốc của nhà máy là anh Junior Vũ muốn biết thời gian sớm nhất để hoàn thiện t đôi giày trên để tính xem có nên bổ sung Robot tiếp không. Các bạn hãy giúp anh ấy nhé!
Đầu vào:
Dòng đầu gồm 2 số nguyên n và t: số con Robot và tổng số đôi giày cần hoàn thiện
Dòng tiếp theo gồm n số nguyên k1, k2, ..., kn: ki - thời gian để con Robot thứ i hoàn thiện xong 1 đôi giày.
Đầu ra:
In ra 1 số nguyên là thời gian ít nhất để hoàn thiện t đôi giày
Ràng buộc:
1 <= n <= 2*10^5
1 <= t <= 10^9
1 <= ki <= 10^9
Ví dụ:
Đầu vào:
3 7
3 2 5
Đầu ra:
8
Giải thích: Con Robot 1 hoàn thiện được 2 đôi giày, Con Robot 2 hoàn thiện được 4 đôi giày và Con Robot 3 hoàn thiện được 1 đôi giày.
*/
#include <bits/stdc++.h>
#define endl "\n"
#define int long
#define long long long
const int MOD = (int)1e9 + 7;
const int limit = 1000001;
using namespace std;
#ifndef ONLINE_JUDGE
    clock_t tStart = clock();
#endif

void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

void sol() {
    int n, t;
    cin >> n >> t;
    vector<int> k(n);

    for (int i = 0; i < n; i++) {
        cin >> k[i];
    }

    int l = 1; // Thời gian ít nhất để hoàn thiện t đôi giày là 1
    //int r = 1LL * t * (*max_element(k.begin(), k.end())); // Thời gian lớn nhất có thể để hoàn thiện t đôi giày
    int max_time_per_pair = *max_element(k.begin(), k.end()); // thời gian ngắn nhất mà một con Robot có thể hoàn thiện một đôi giày.
    int r = 1LL * t * max_time_per_pair; //  Nhân thời gian ngắn nhất của một đôi giày với số lượng đôi giày cần hoàn thiện 
    int res = r;

    while (l <= r) {
        int m = (l + r) / 2;
        int totalPairs = 0;

        for (int i = 0; i < n; i++) {
            totalPairs += m / k[i];
            if (totalPairs >= t) {
                break;
            }
        }

        if (totalPairs >= t) {
            res = m;
            r = m - 1;
        } else {
            l = m + 1;
        }
    }

    cout << res;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

