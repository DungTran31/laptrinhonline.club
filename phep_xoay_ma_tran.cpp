void rotateMatrix(vector<vector<int>>& a, int alpha) {
    int n = a.size();
    int m = a[0].size();
    
    int numRotations = alpha / 90;
    numRotations = (numRotations % 4 + 4) % 4; // Đảm bảo numRotations nằm trong khoảng từ 0 đến 3
    
    while (numRotations--) {
        vector<vector<int>> rotated(a[0].size(), vector<int>(a.size(), 0));
        
        for (int i = 0; i < a.size(); i++) {
            for (int j = 0; j < a[0].size(); j++) {
                rotated[j][a.size() - i - 1] = a[i][j];
            }
        }
        a = rotated;
        swap(n, m);
    }
}

void sol() {
    int n, m, alpha;
    cin >> n >> m >> alpha;
    
    vector<vector<int>> a(n, vector<int>(m));
    
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            cin >> a[i][j];
        }
    }
    
    rotateMatrix(a, alpha);
    
    for (int i = 0; i < a.size(); i++) {
        for (int j = 0; j < a[0].size(); j++) {
            cout << setw(6) << a[i][j];
        }
        cout << endl;
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

