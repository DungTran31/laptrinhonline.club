// công thức chỉnh hợp chập 3 của n
unsigned int c(int n, int k) {
    if (k > n) return 0; // Trường hợp không thể chọn k phần tử từ n phần tử
    if (k == 0 || k == n) return 1; 
    unsigned int result = 1;
    for (int i = 0; i < k; ++i) {
        result *= (n - i);
        result /= (i + 1);
    }
    return result;
}

void sol(){
    int n; cin >> n;
    cout << c(n, 3);
}

