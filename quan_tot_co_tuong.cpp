#include <bits/stdc++.h>
#define endl "\n"
#define int long
#define long long long
const int MOD = (int)1e9 + 7;
const int limit = 1000001;
using namespace std;
#ifndef ONLINE_JUDGE
    clock_t tStart = clock();
#endif

void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

const int N = 10;
int x, y;
int d[N][N];
pair<int, char> ans = {INT_MAX, 'Z'};

void bfs(int i, int j, char c) {
    memset(d, -1, sizeof(d));

    queue<pair<int, int>> q;
    q.push({i, j});
    d[i][j] = 0;

    while (!q.empty()) {
        int u = q.front().first;
        int v = q.front().second;
        q.pop();

        if (u + 1 < N && d[u + 1][v] == -1) {
            d[u + 1][v] = d[u][v] + 1;
            q.push({u + 1, v});
        }
        if (u >= 5 && v - 1 >= 0 && d[u][v - 1] == -1) {
            d[u][v - 1] = d[u][v] + 1;
            q.push({u, v - 1});
        }
        if (u >= 5 && v + 1 < N - 1 && d[u][v + 1] == -1) {
            d[u][v + 1] = d[u][v] + 1;
            q.push({u, v + 1});
        }
    }

    if (d[x][y] != -1) {
        ans = min(ans, make_pair(d[x][y], c));
    }
}

void sol() {
    cin >> x >> y;

    bfs(3, 0, 'A');
    bfs(3, 2, 'B');
    bfs(3, 4, 'C');
    bfs(3, 6, 'D');
    bfs(3, 8, 'E');

    if (ans.first == INT_MAX) {
        cout << "-1\n";
    } else {
        cout << ans.first << "\n";
        cout << ans.second;
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

