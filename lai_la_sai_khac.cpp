void sol() {
    int n;
    cin >> n;
    int a[n];

    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }

    sort(a, a+n);
    int sum = 0;
    int coeff = -1 * (n - 1);
    for (int i = 0; i < n; ++i) {
        sum += coeff * a[i];
        coeff += 2;
    }
    cout << sum;
}

