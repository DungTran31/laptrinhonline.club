int minStepsToTransform(int n, int m) {
    if (n < 0 || m < 0) {
        return -1; // Trường hợp n hoặc m âm, không thể biến đổi
    }

    if (n == m) {
        return 0; // Trường hợp n và m đã bằng nhau
    }

    vector<int> dist(2 * max(n, m) + 1, -1);
    queue<int> q;

    q.push(n);
    dist[n] = 0;

    while (!q.empty()) {
        int cur = q.front();
        q.pop();

        // Cách 1: Giảm đi 1
        if (cur - 1 >= 0 && dist[cur - 1] == -1) {
            dist[cur - 1] = dist[cur] + 1;
            q.push(cur - 1);
        }

        // Cách 2: Nhân đôi
        if (cur * 2 <= 2 * max(n, m) && dist[cur * 2] == -1) {
            dist[cur * 2] = dist[cur] + 1;
            q.push(cur * 2);
        }

        if (dist[m] != -1) {
            return dist[m];
        }
    }

    return -1; // Không thể biến đổi thành m
}

void sol(){
    int n, m;
    cin >> n >> m;

    int res = minStepsToTransform(n, m);

    if (res == -1) {
        cout << "ERROR" << endl;
    } else {
        cout << res << endl;
    }
}

