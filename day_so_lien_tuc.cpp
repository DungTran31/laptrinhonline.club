bool isNumberSequence(string s, string &start) {
    if (s.size() == 1 || s[0] == '0') return false;
    int n = 0;
    bool isSequence = 0;
    for (int i = 0; i < s.length()/2; i++){
        n = n * 10 + (s[i] - '0');
        string t = to_string(n);
        int d = n + 1;
        while (t.size() < s.size()){
            t+=to_string(d);
            d++;
        }
        if (t.size() != s.size()) continue;
        else if (t == s){
            isSequence=true;
            break;
        }
    }
    start = to_string(n);
    if(!isSequence) return false;
    else return true;
}

void sol() {
    int t;
    cin >> t;
    while (t--){
        string s;
        cin >> s;
        string x = "";
        if(isNumberSequence(s, x)) cout << "YES " << x << endl;
        else cout << "NO" << endl;
    }
}

