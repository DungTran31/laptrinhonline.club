void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

int power(int base, int exp){
    if(exp == 1) return base%MOD;
    if(exp == 0) return 1;
    int tmp = power(base, exp/2) % MOD;
    if(exp & 1) return tmp%MOD*tmp%MOD*base%MOD;
    else return tmp%MOD*tmp%MOD;
}

void sol() {
    string a, b;
    cin >> a >> b;
    int x = 0, y = 0;
    for (auto c : a) {
        x = (x * 10 + (c - '0')) % MOD;
    }
    for (auto c : b) {
        y = (y * 10 + (c - '0')) % (MOD-1);
    }
    int res = power(x, y);
    cout << res;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

