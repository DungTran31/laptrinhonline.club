void sol() {
    int n; cin >> n;
    vector<int> a(n);
    vector<int> b;
    vector<int> c;
    vector<int> d;
    for (int i = 0; i < n; i++) {
        cin >> a[i];
        if(a[i]%3==0) b.push_back(a[i]);
        else if(a[i]%3==1) c.push_back(a[i]);
        else d.push_back(a[i]);
    }
    sort(b.begin(), b.end());
    sort(c.begin(), c.end());
    sort(d.begin(), d.end());
    for (int i = 0; i < b.size(); i++) {
        cout << b[i] << " ";
    }
    for (int i = 0; i < c.size(); i++) {
        cout << c[i] << " ";
    }
    for (int i = 0; i < d.size(); i++) {
        cout << d[i] << " ";
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

