// Hàm tính định thức ma trận
double tinhD(double a[][101], int n) {
    int i, j, k, dem = 0, kt = 0;
    double b[101], c[101], h, det = 1;

    for (i = 0; i < n - 1; i++) {
        if (a[i][i] == 0) {
            for (j = 0; j < n; j++) {
                if (a[i][j] != 0) {
                    for (k = 0; k < n; k++) {
                        c[k] = a[k][i];
                        a[k][i] = a[k][j];
                        a[k][j] = c[k];
                    }
                    dem++;
                    kt++;
                    break;
                }
            }
            if (kt == 0) return 0;
        }

        b[i] = a[i][i];
        for (j = 0; j < n; j++) a[i][j] = a[i][j] / b[i];

        for (j = i + 1; j < n; j++) {
            h = a[j][i];
            for (k = 0; k < n; k++) a[j][k] = a[j][k] - h * a[i][k];
        }
    }

    b[n - 1] = a[n - 1][n - 1];
    for (i = 0; i < n; i++) det *= b[i];

    if (dem % 2 == 0) return det;
    else return -det;
}

void sol() {
    double a[101][101];
    int n, i, j;
    cin >> n;

    for (i = 0; i < n; i++) {
        for (j = 0; j < n; j++) {
            cin >> a[i][j];
        }
    }

    cout << fixed << setprecision(2) << tinhD(a, n) << endl;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

