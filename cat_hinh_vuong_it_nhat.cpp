int minSquares(int m, int n) {
    int dp[m + 1][n + 1];
    
    for (int i = 0; i <= m; ++i) {
        for (int j = 0; j <= n; ++j) {
            if (i == j) {
                dp[i][j] = 1;  // Hình vuông cạnh nguyên
            } else {
                dp[i][j] = i * j;  // Khởi tạo giả định số hình vuông cạnh nguyên khi chưa cắt
                for (int k = 1; k <= min(i, j); ++k) {
                    dp[i][j] = min(dp[i][j], 1 + dp[i - k][j] + dp[k][j - k]);
                    dp[i][j] = min(dp[i][j], 1 + dp[i][j - k] + dp[i - k][k]);
                }
            }
        }
    }

    return dp[m][n];
}

void sol() {
    int m, n;
    cin >> m >> n;

    int res = minSquares(m, n);

    cout << res;
}

