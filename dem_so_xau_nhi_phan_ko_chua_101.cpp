void sol() {
    int n;
    cin >> n;
    int a[n+1];
    a[1] = 2;
    a[2] = 4;
    a[3] = 7;
    for (int i = 4; i <= n; i++) {
        a[i] = (2 * a[i-1] - a[i-2] + a[i-3]) % MOD;
    }
    cout << (a[n] + MOD) % MOD;
}

