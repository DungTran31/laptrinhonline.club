void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

void sol() {
    int n, m, sum = 0, d = 0;
    cin >> n >> m;

    int a[n + 2];
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
    }

    for (int i = 1; i <= n; i++) {
        if (a[i] + d < m) {
            sum += a[i] + d;
            d = 0;
        } else {
            if (d > m) {
                sum += m;
                d = a[i];
            } else {
                sum += m;
                d = a[i] + d - m;
            }
        }
    }

    if (d > m) {
        sum += m;
    } else {
        sum += d;
    }
    cout << sum << endl;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

