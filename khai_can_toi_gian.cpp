void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

void sol() {
    int n, k;  
    cin >> n >> k;
    int x = n;
    unordered_map<int, int> mp;  

    // Tính phân tích thừa số nguyên tố của n
    for (int i = 2; i * i <= n; i++) {
        while (n % i == 0) {
            mp[i]++;
            n /= i;
        }
    }

    int res = 1;
    // Tính căn bậc k của các thừa số nguyên tố
    for (auto factor : mp) {
        int prime = factor.first;
        int exponent = factor.second;
        res *= pow(prime, exponent / k);
    }

    cout << res << " " << llround(x / pow(res, k)) << endl;  // Sử dụng llround để làm tròn
    
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

