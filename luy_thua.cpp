#include <bits/stdc++.h>
#define endl "\n"
#define ull unsigned long long
const int MOD = 1000000007;
using namespace std;
#ifndef ONLINE_JUDGE
    clock_t tStart = clock();
#endif

void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

// Hàm tính (x * y) % MOD
ull multiplyMod(ull x, ull y) {
    x %= MOD;
    y %= MOD;
    return (x * y) % MOD;
}

// Hàm tính a^b % MOD với a và b là chuỗi
ull powerMod(string a, string b) {
    ull base = 0;
    for (char digit : a) {
        base = (base * 10 + (digit - '0')) % MOD;
    }

    ull exp = 0;
    for (char digit : b) {
        exp = (exp * 10 + (digit - '0')) % (MOD - 1); // Phần mũ MOD - 1 vì tính chất của modulo
    }

    ull res = 1;
    while (exp > 0) {
        if (exp % 2 == 1) {
            res = multiplyMod(res, base);
        }
        base = multiplyMod(base, base);
        exp /= 2;
    }

    return res;
}

void sol() {
    int t; cin >> t;
    while (t--){
        string a, b; cin >> a >> b;
        
        ull res = powerMod(a, b);
        cout << res << endl;
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

