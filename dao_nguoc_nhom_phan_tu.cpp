void reverseGroup(vector<int>& a, int n, int k) {
    for (int i = 0; i < n; i += k) {
        int left = i;
        int right = min(i + k - 1, n - 1);

        while (left < right) {
            swap(a[left], a[right]);
            left++;
            right--;
        }
    }
}


void sol() {
    int n, k;
    cin >> n >> k;

    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    reverseGroup(a, n, k);

    for (int i = 0; i < n; i++) {
        cout << a[i] << " ";
    }
}

