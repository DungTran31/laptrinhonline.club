void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

void sol() {
    int n;
    cin >> n;
    int a[n];
    for (int &x : a) cin >> x;
    
    set<int> st;
    unordered_map<int, int> check;
    
    st.insert(a[0]);
    check[a[0]] = 0;
    
    for (int &x : a) {
        auto it = st.lower_bound(x);
        if (*it == x) continue;
        
        if (it == st.end()) {
            check[x] = check[*(--it)];
        } else if (it == st.begin()) {
            check[x] = check[*it];
        } else {
            check[x] = check[*it];
            check[x] = max(check[x], check[*(--it)]);
        }
        check[x]++;
        st.insert(x);
    }
    
    for (auto x : st) {
        cout << x << " " << check[x] << endl;
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

