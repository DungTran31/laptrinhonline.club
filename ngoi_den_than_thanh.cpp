void sol() {
    string a;
    cin >> a;
    int n = a.size();
    string x, y;
    int i, j;
    for(i = n - 1; i >= 1; i--){
        x = a.substr(0, i);
        y = a.substr(n - i, i);
        if(x != y) continue;
        for(j = 1; j < n - i; j++){
            if(x == a.substr(j, i)){
                cout << x;
                return;
            }
        }
    }
    cout << "Just a legend";
}

