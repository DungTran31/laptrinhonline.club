#ifndef ONLINE_JUDGE
    clock_t tStart = clock();
#endif

void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

void sol() {
    int n;
    cin >> n;

    vector<int> colors(n);
    for (int i = 0; i < n; ++i) {
        cin >> colors[i];
    }

    // Đếm số lượng màu xuất hiện nhiều nhất
    int maxCount = 0;
    for (int i = 0; i < n; ++i) {
        int cnt = 1;
        int j = i;
        // Đếm số lượng cây bút chì giống nhau liên tiếp từ trái sang phải
        while (j + 1 < n && colors[j] == colors[j + 1]) {
            cnt++;
            j++;
        }
        maxCount = max(maxCount, cnt);
    }

    // Kết quả là tổng số cây bút chì trừ đi số lượng màu xuất hiện nhiều nhất
    int res = n - maxCount;
    cout << res << endl;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

