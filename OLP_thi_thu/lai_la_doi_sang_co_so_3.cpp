string convertToBase3(int n) {
    if (n == 0) return "0"; 

    string res = ""; 

    while (n > 0) {
        int remainder = n % 3; 
        res = to_string(remainder) + res; 
        n /= 3; 
    }

    return res;
}

string subtractOfBits(string a, string b) {
    string result = "";

    for (int i = a.length() - 1; i >= 0; --i) {
        int digitA = a[i] - '0';
        int digitB = b[i] - '0';
        if(digitA > digitB) digitB += 3;
        int sum = digitB - digitA;
        int bitSum = sum % 3;
        result = to_string(bitSum) + result;
    }

    return result;
}

int base3ToBase10(string number) {
    int res = 0;
    int power = 0;
    
    for (int i = number.length() - 1; i >= 0; i--) {
        res += (number[i] - '0') * pow(3, power);
        power++;
    }
    
    return res;
}

