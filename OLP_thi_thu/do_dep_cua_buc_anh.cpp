int solve(vector<pair<int, int>> a) {
    int res = 0;

    for (int i = 0; i < a.size(); ++i) {
        for (int j = i + 1; j < a.size(); ++j) {
            int dx = a[i].first - a[j].first;
            int dy = a[i].second - a[j].second;
            res += dx * dx + dy * dy; 
        }
    }

    return res;
}

