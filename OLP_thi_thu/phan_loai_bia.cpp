int solve(vector<pair<double, double>> a) {
    int n = a.size();
    vector<int> res(n, 1);

    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < i; ++j) {
            if (a[i].first > a[j].first && a[i].second < a[j].second) {
                res[i] = max(res[i], res[j] + 1);
            }
        }
    }

    return *max_element(res.begin(), res.end());
}

void sol() {
    int t;
    cin >> t; 

    while (t--) {
        int n;
        cin >> n; 
        vector<pair<double, double>> a;

        for (int i = 0; i < n; ++i) {
            double x, y;
            cin >> x >> y;
            a.push_back({x, y});
        }

        int res = solve(a);

        cout << res << endl;
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

