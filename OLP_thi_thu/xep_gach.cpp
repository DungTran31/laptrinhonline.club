void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

bool cmp(pair<int, double> a, pair<int, double> b) {
    return a.second < b.second;
}

void sol() {
    int n, m;
    cin >> n >> m;
    vector<pair<int, double>> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i].first >> a[i].second;
    }
    sort(a.begin(), a.end(), cmp);
    vector<int> dp(n, 1);
    for (int i = 1; i < n; ++i) {
        for (int j = 0; j < i; ++j) {
            if (a[i].first >= a[j].first && dp[i] < dp[j] + 1) {
                dp[i] = dp[j] + 1;
            }
        }
    }
    int maxRes = INT_MIN;
    for(int i = 0; i < dp.size(); i++) {
        maxRes = max(maxRes, dp[i]);
    }
    cout << n - maxRes;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

