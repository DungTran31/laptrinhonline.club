void sol() {
    int n, k;
    cin >> n >> k;

    vector<int> a;
    for (int i = 0; i < n; ++i) {
        int x; cin >> x;
        if(x >= k) a.push_back(x);
    }
    int res = 0;
    for (int i = 0; i < a.size(); i++) {
        res += (a[i]/k) * 3;
    }
    
    cout << res * k << endl;
    
}

