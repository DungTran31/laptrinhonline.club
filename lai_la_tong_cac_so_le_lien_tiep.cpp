void sol() {
    int a, b; cin >> a >> b;
    if (a>b) swap(a,b);
    if (a % 2 != 0) a+=2;
    else a++;
    if (b % 2 != 0) b-=2;
    else b--;
    
    int sum = 0;
    for (int i = a; i <= b; i+=2) {
        sum += i;
    }
    cout << sum;
}

