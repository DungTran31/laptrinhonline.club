void sol() {
    int n;
    cin >> n;
    // Vẽ phần trên của hình trám
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n - i; j++) {
            cout << " ";
        }
        cout << "*";
        if (i > 1) {
            for (int j = 1; j <= 2 * i - 3; j++) {
                cout << " ";
            }
            cout << "*";
        }
        cout << endl;
    }

    // Vẽ phần dưới của hình trám
    for (int i = n - 1; i >= 1; i--) {
        for (int j = 1; j <= n - i; j++) {
            cout << " ";
        }
        cout << "*";
        if (i > 1) {
            for (int j = 1; j <= 2 * i - 3; j++) {
                cout << " ";
            }
            cout << "*";
        }
        cout << endl;
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

