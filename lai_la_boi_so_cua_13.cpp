void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

int csc(int a, int b, int d){
    return ((b - a) / d + 1) * (b + a) / 2;
}

void sol() {
    int a, b, x, y, s;
    cin >> a >> b;

    if (a > b) {
        x = b;
        y = a;
    } else {
        x = a;
        y = b;
    }

    s = csc(x, y, 1);
    while (x % 13)
        x++;
    while (y % 13)
        y--;

    cout << s - csc(x, y, 13);
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

