void sol() {
    int t0, sum = 0;
    string s;
    cin >> t0 >> s;

    for (char c : s) {
        sum += (c == '1') ? 1 : 0;
    }

    int n = s.length();
    if (sum % 2 == 0 || (t0 % 2 == 0 && n % 2 == 0)) {
        cout << s;
    } else {
        for (char c : s) {
            cout << (c == '1' ? '0' : '1');
        }
    }
}

