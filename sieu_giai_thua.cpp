#include <bits/stdc++.h>
#define endl "\n"
#define ull unsigned long long
const int MOD = (int)1e9 + 7;
const int limit = 1000001;
using namespace std;
#ifndef ONLINE_JUDGE
    clock_t tStart = clock();
#endif

void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

void sol() {
    ull n;
	cin >> n;
    // Khai báo và khởi tạo biến count5 để theo dõi số lượng chữ số 0 tận cùng trong x!
	ull count5 = 0, temp = 5, x=5*n - 5*(n/5);
	while(true){
        // Khởi tạo count5 và temp trở lại để tính lại số lượng chữ số 0.
		count5 = 0;
		temp = 5;
		while(true){
			ull t = x / temp;
			if(t==0){
				break;
			}
            // Tính số lượng chữ số 0 trong x! bằng cách chia x cho temp và cộng giá trị t vào count5.
			count5 += t;
			temp *= 5;
		}
		if(count5 == n){
			cout << x;
			break;
		}
		x += 5;
	}
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

