int solve(int n){
    if(n == 0) return 0;
    int res;

    if(n % 2 == 1) {
        res = (n + 1) / 2 * (n + 1) / 2;
    }else {
        res = n / 2 * n / 2;
    }

    return res + solve(n / 2);
}

