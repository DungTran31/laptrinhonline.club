using namespace std;
#ifndef ONLINE_JUDGE
    clock_t tStart = clock();
#endif

void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

bool cmp(pair<int, int> j1, pair<int, int> j2) {
    return (j1.second < j2.second);
}

void sol() {
    int t; cin >> t;
    while (t--) {
        int n;
        cin >> n;

        vector<pair<int, int>> a(n);

        for (int i = 0; i < n; i++) {
            cin >> a[i].first >> a[i].second;
        }

        sort(a.begin(), a.end(), cmp);

        int cnt = 0; 
        int prevStart1 = -1, prevStart2 = -1;

        for (int i = 0; i < n; i++) {
            if (prevStart1 < prevStart2)
                swap(prevStart1, prevStart2);
            if (a[i].first > prevStart1) {
                cnt++;
                prevStart1 = a[i].second;
            } else if (a[i].first > prevStart2) {
                cnt++;
                prevStart2 = a[i].second;
            }
        }

        cout << cnt << endl;
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

