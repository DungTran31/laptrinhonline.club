struct Node {
    char ch;
    int freq;
    Node* left;
    Node* right;
};

struct Compare {
    bool operator()(Node* a, Node* b) {
        return a->freq > b->freq;
    }
};

map<char, int> getFrequencies(string s) {
    map<char, int> freq;
    for (char c : s) {
        freq[c]++;
    }
    return freq;
}

Node* buildHuffmanTree(map<char, int> freq) {
    priority_queue<Node*, vector<Node*>, Compare> pq;

    for (auto pair : freq) {
        Node* n = new Node;
        n->ch = pair.first;
        n->freq = pair.second;
        n->left = nullptr;
        n->right = nullptr;
        pq.push(n);
    }

    while (pq.size() > 1) {
        Node* left = pq.top();
        pq.pop();
        Node* right = pq.top();
        pq.pop();

        Node* internalNode = new Node;
        internalNode->ch = '\0';
        internalNode->freq = left->freq + right->freq;
        internalNode->left = left;
        internalNode->right = right;

        pq.push(internalNode);
    }

    return pq.top();
}

int getBitCount(Node* root, int depth = 0) {
    if (!root) {
        return 0;
    }
    if (!root->left && !root->right) {
        return root->freq * depth;
    }
    return getBitCount(root->left, depth + 1) + getBitCount(root->right, depth + 1);
}

