void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

map<int, int> F; 
 
int fib(int n) {  
    if(F.count(n)) return F[n];  
    int k = n / 2;  
    if(n % 2 == 0){  // n=2*k   
        return F[n] = (fib(k) * fib(k) + fib(k-1) * fib(k-1)) % MOD;  
    }else{ // n=2*k+1   
        return F[n] = (fib(k) * fib(k+1) + fib(k-1) * fib(k)) % MOD; 
    } 
} 

int power(int x, int n) {
    int res = 1;
    while (n) {
        if (n & 1)
            res = (res * x) % MOD;
        n = n / 2;
        x = (x * x) % MOD;
    }
    return res;
}

void sol() {
    int t;
    cin >> t;
    F[0]=F[1]=1;
    while (t--) {
        int n;
        cin >> n;
        cout << (power(2, n) - fib(n + 2) + 1 + MOD) % MOD << endl;
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

