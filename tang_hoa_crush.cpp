void runtime()
{
#ifndef ONLINE_JUDGE
    fprintf(stderr, ">> Runtime: %.10fs\n", (double)(clock() - tStart) / CLOCKS_PER_SEC);
#endif
}

void sol(){
    int n, m;
    cin >> n >> m;
    int a[n + 1];
    a[0] = 0;
    for (int i = 1; i <= n; i++){
        cin >> a[i];
        a[i] += a[i - 1];
    }
    int f[m];
    for (int i = 0; i < m; i++){
        int x, y;
        cin >> x >> y;
        f[i] = a[y] - a[x - 1];
    }
    sort(f, f + m);
    int sum = 0;
    for (int i = 0; i < m; i++){
        if (f[i] >= 0)
            sum += f[i];
    }
    cout << sum;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

