using namespace std;
#ifndef ONLINE_JUDGE
    clock_t tStart = clock();
#endif

void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

void sol() {
    int n;
    cin >> n;

    vector<string> numbers;
    for (int i = 0; i < n; ++i) {
        string s; cin >> s;
        numbers.push_back(s);
    }
    // Sắp xếp chuỗi theo thứ tự giảm dần
    // VD: x = "48", y="7" so sánh (x+y) = "487" và (y+x) = "748"
    sort(numbers.begin(), numbers.end(), [](const string& x, const string& y) {
        return (x + y) > (y + x);
    });

    // Ghép các chuỗi đã sắp xếp
    stringstream result;
    for (string num : numbers) {
        result << num;
    }

    cout << result.str() << endl;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

