void runtime()
{
#ifndef ONLINE_JUDGE
    fprintf(stderr, ">> Runtime: %.10fs\n", (double)(clock() - tStart) / CLOCKS_PER_SEC);
#endif
}

void sol(){
    int n;
    cin >> n;
    int a[n + 5] = {};
    pair<int, int> M[n + 5] = {};

    for (int i = 1; i <= n; i++) {
        cin >> a[i];
        M[i].first = a[i];
        M[i].second = i;
    }

    sort(M + 1, M + n + 1);
    int dem = 0;

    for (int i = 1; i <= n; i++) {
        if (a[M[i].second] != 0) {
            a[M[i].second - 1] = 0;
            a[M[i].second + 1] = 0;
            dem++;
        }
    }

    cout << dem;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

