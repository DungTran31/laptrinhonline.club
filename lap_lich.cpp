void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

bool cmp(pair<int, int> j1, pair<int, int> j2) {
    return (j1.second < j2.second);
}

void sol() {
    int n;
    cin >> n;

    vector<pair<int, int>> a(n);

    for (int i = 0; i < n; i++) {
        cin >> a[i].first >> a[i].second;
    }

    sort(a.begin(), a.end(), cmp);

    int cnt = 1; 
    int prevEnd = a[0].second;

    for (int i = 1; i < n; i++) {
        if (a[i].first > prevEnd) {
            cnt++;
            prevEnd = a[i].second;
        }
    }

    cout << cnt << endl;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

