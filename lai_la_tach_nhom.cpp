void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

int cnt = 0;

void solve(int n, int k) {
    if (n < k + 2 || (n - k) % 2 != 0) {
        cnt += 1;
        return;
    }
    solve((n + k) >> 1, k);
    solve((n - k) >> 1, k);
}

void sol() {
    int n, k;
    cin >> n >> k;
    if (n == k) {
        cout << "1";
        return;
    }
    if (n < k + 2) {
        cout << "0";
        return;
    }
    solve(n, k);
    cout << cnt;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}
