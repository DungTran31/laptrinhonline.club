void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

void sol() {
    int n;
    cin >> n;
    int _value;
    vector<int> a;
    int sum = 0;
    for (int i = 0; i < n; ++i) {
        int x,y;
        cin >> x >> y;
        _value = abs(x-y);
        sum += _value;
        a.push_back(_value);
        while (a.size() >= 3 && a[a.size() - 3] <= a[a.size() - 2] && a[a.size() - 2] >= a[a.size() - 1]) {
            int val = a[a.size() - 3] + a[a.size() - 1] - a[a.size() - 2];
            a.pop_back();
            a.pop_back();
            a.pop_back();
            a.push_back(val);
        }
    }
    int l = 0, r = a.size() - 1;
    int score1 = 0;
    bool turn = 0;
    while (l <= r) {
        if (a[l] >= a[r]) {
            score1 += a[l] * (turn == 1 ? -1 : 1);
            l++;
        } else {
            score1 += a[r] * (turn == 1 ? -1 : 1);
            r--;
        }
        turn = !turn;
    }
    int ans,ans2;
    ans = (sum - score1) / 2 + score1;
    ans2 = sum - ans;
    ans %= MOD;
    ans2 %= MOD;
    cout << ans << "\n" << ans2 <<"\n";
    if(ans > ans2) cout << "Ken";
    else if(ans < ans2) cout << "Kun";
    else cout << -1;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

