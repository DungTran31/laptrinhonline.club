void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

int mymax(int a, int b) {
    return a > b ? a : b;
}

void sol() {
    int n , res = 0;
    cin >> n; 
    vector<int> a(n,0), l(n,0), r(n,0);
    for(int i = 0; i < n; i++) cin >> a[i];
    partial_sum(a.begin(), a.end(), l.begin(), mymax);
    partial_sum(a.rbegin(), a.rend(), r.rbegin(), mymax);
    
    for(int i = 1; i < n - 1; i++) {
        if(l[i - 1] > a[i] && r[i + 1] > a[i]) {
            res += min(l[i - 1],r[i + 1])-a[i];
        }
    }
    cout << res;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

