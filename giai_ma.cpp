bool check(int n, int target) {
	int res = n;
	while(n) {
		res += n%10;
		n/=10;
	}
	return target==res;
}

int solve(int n) {
	for(int i = 1; i <= 81; i++) {
		if(n-i < 0) return -1;
		if(check(n-i, n)) return n-i;
	}
	return -1;
}
void sol() {
    int n; cin >> n;
    cout << solve(n);
}

