using namespace std;
#ifndef ONLINE_JUDGE
    clock_t tStart = clock();
#endif

void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

void sol() {
    int n, k;
    cin >> n >> k;

    vector<int> am, duong;
    for (int i = 0; i < n; ++i) {
        int x;
        cin >> x;
        (x >= 0 ? duong : am).push_back(abs(x));
    }

    am.push_back(0);
    duong.push_back(0);
    sort(am.begin(), am.end());
    sort(duong.begin(), duong.end());

    int res = LLONG_MAX;
    for (int i = 0; i <= k; ++i) {
        if (i < duong.size() && (k - i) < am.size()) {
            int curr = min(2 * duong[i] + am[k - i], duong[i] + 2 * am[k - i]);
            res = min(res, curr);
        }
    }

    cout << res << endl;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

