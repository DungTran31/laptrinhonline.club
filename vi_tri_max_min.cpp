pair<int, int> solve(vector<int> a) {
    int minElement = INT_MAX, maxElement = INT_MIN;
    int minIndex = -1, maxIndex = -1;

    for (int i = 0; i < a.size(); ++i) {
        if (a[i] < minElement) {
            minElement = a[i];
            minIndex = i;

        }
        if (a[i] > maxElement) {
            maxElement = a[i];
            maxIndex = i;
        }
    }

    return make_pair(minIndex, maxIndex); 
}

