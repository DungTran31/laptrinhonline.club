void sol() {
    int n; cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    int res = 0;
    int z = -INT_MAX;
    for(int  i = n - 1; i >= 0; i--) {
        if(a[i] > z) z = a[i];
        else if(a[i] <= z) res += (z - a[i]);
    }
    cout << res;
}

