#ifndef ONLINE_JUDGE
    clock_t tStart = clock();
#endif


void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

int isSum(int a, int b, char c) { 
    return c == 43 ? a + b : a - b; 
}

void sol() {
    stack<char> s1; // Ngăn xếp lưu trữ dấu
    stack<int> s2; // Ngăn xếp lưu trữ số
    string s; cin >> s;
    for (int i = 0; i < s.length(); i++) {
        if (s[i] >= '0' && s[i] <= '9') {
            int num = 0;
            while (i < s.length() && s[i] >= '0' && s[i] <= '9') {
                num = num * 10 + (s[i] - '0');
                i++;
            }
            s2.push(num);
            i--; // Quay lại ký tự trước vị trí hiện tại vì vòng lặp chưa tăng i
        } else if (s[i] == '(') {
            s1.push(s[i]);
        } else if (s[i] == ')') {
            while (s1.top() != '(') {
                int b = s2.top(); s2.pop();
                int a = s2.top(); s2.pop();
                char op = s1.top(); s1.pop();
                s2.push(isSum(a, b, op));
            }
            s1.pop(); // Loại bỏ dấu "(" khỏi ngăn xếp
        } else if (s[i] == '+' || s[i] == '-') {
            while (!s1.empty() && (s1.top() == '+' || s1.top() == '-')) {
                int b = s2.top(); s2.pop();
                int a = s2.top(); s2.pop();
                char op = s1.top(); s1.pop();
                s2.push(isSum(a, b, op));
            }
            s1.push(s[i]);
        }
    }

    while (!s1.empty()) {
        int b = s2.top(); s2.pop();
        int a = s2.top(); s2.pop();
        char op = s1.top(); s1.pop();
        s2.push(isSum(a, b, op));
    }

    cout << s2.top();
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

