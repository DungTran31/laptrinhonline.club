bool isMirrorString(string str) {
    int n = str.length();
    for (int i = 0; i < n / 2; ++i) {
        if (str[i] != str[n - i - 1]) {
            return false;
        }
    }
    return true;
}

void sol() {
    int t;
    cin >> t;

    while (t--) {
        string str;
        cin >> str;

        int n = str.length();
        int maxLen = 0;
        int cnt = 0;

        for (int i = 0; i < n; ++i) {
            for (int j = i; j < n; ++j) {
                string sub = str.substr(i, j - i + 1);
                if (isMirrorString(sub)) {
                    int subLen = j - i + 1;
                    if (subLen > maxLen) {
                        maxLen = subLen;
                        cnt = 1;
                    } else if (subLen == maxLen) {
                        cnt++;
                    }
                }
            }
        }

        cout << maxLen << " " << cnt << endl;
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

