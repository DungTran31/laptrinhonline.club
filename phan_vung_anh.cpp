int a[205][205], n, m;    

int dfs(int u, int v) {
    int d = 1;
    stack<pair<int, int>> S;
    a[u][v] = 1; // đã xét 1 th
    S.push({u, v});
    while (S.size() > 0) {
        int x = S.top().first, y = S.top().second;
        S.pop();
        for(int i = -1; i <= 1; i++) {
            for(int j = -1; j <= 1; j++) {
                if(1 <= x+i && x+i <=n && 1<=y+j && y+j<=m && a[x+i][y+j] == 0) {
                    a[x+i][y+j] = 1;
                    S.push({x+i,y+j});
                    d++;
                }
            }
        }
    }    
    return d;
}


void sol() {
    cin >> n >> m;
    for(int i = 1; i <= n; i++){
        for(int j = 1; j <= m; j++){
            cin >> a[i][j];
        }
    }
    int k = 0, G[n*m+4] = {};
    for(int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            if(a[i][j] == 0){
                k++;
                G[k] = dfs(i, j);
            }
        }
    }
    cout << k << endl;
    sort(G + 1, G + k + 1);
    for(int i = 1; i <= k; i++){
        cout << G[i] << " ";
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

