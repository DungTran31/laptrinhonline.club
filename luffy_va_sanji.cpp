void sol() {
    int n; cin >> n;
    for (int i = 0; i < n; i++) {
        int a, b; cin >> a >> b;
        int x=round(pow(a*b,1.0/3));     
        if (x*x*x==a*b && a%x==0 && b%x==0) cout << "Yes" << endl;
        else cout << "No" << endl;
    }    
}

