void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

string simplifyPath(string path) {
    stringstream ss(path);
    string token;
    stack<string> stk;

    while (getline(ss, token, '/')) {
        if (token == "" || token == ".") {
            // Do nothing for empty tokens or current directory reference
            continue;
        } else if (token == "..") {
            // Move up one directory level
            if (!stk.empty()) {
                stk.pop();
            }
        } else {
            // Push valid directory/file to stack
            stk.push(token);
        }
    }

    // Reconstruct the simplified path
    vector<string> v;
    while (!stk.empty()) {
        v.push_back(stk.top());
        stk.pop();
    }

    string result = "";
    for (auto it = v.rbegin(); it != v.rend(); ++it) {
        result += "/" + *it;
    }

    return result.empty() ? "/" : result;
}

void sol() {
    string s; cin >> s;
    cout << simplifyPath(s);
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

