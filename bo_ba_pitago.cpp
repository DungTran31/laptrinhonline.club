void sol(){
    int n, a[201];
    cin >> n;
    int dem = 0;
    for (int i = 0; i < n; i++)
        cin >> a[i];
    for (int i = 0; i < n - 2; i++)
        for (int j = i + 1; j < n - 1; j++)
            for (int k = j + 1; k < n; k++)
                if ((a[i] * a[i] + a[j] * a[j] == a[k] * a[k]) || (a[i] * a[i] == a[j] * a[j] + a[k] * a[k]) || (a[i] * a[i] + a[k] * a[k] == a[j] * a[j]))
                    dem++;
    cout << dem;
}

