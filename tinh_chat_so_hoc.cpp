int pow_mod(int a,int b){   // a^b
    if (b==0) return 1;       //a^0 = 1
    if (b==1) return a % MOD;   //a^1 = a
    int temp = pow_mod(a, b/2);  //tính a^(b/2)
    return temp * temp % MOD * pow_mod(a, b % 2) % MOD;
}

int div_mod(int a,int b){   // a/b
    return a * pow_mod(b, MOD-2) % MOD;
}

