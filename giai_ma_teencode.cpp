#include <bits/stdc++.h>
#define endl "\n"
const int MOD = (int)1e9 + 7;
const int limit = 1000001;
using namespace std;
#ifndef ONLINE_JUDGE
    clock_t tStart = clock();
#endif

void runtime(){
    #ifndef ONLINE_JUDGE
        fprintf(stderr, ">> Runtime: %.10fs\n", (double) (clock() - tStart) / CLOCKS_PER_SEC);
    #endif
}

void to_lower(string &s) {
	for(char &x:s) {
		if(x >= 'A' && x <= 'Z') {
            x = char(x+32);
        }
	}
}

void sol() {
    int n; 
    cin >> n;
    cin.ignore();
    string s; 
    getline(cin,s); 
    to_lower(s);
    multimap<string,int> mp;
    while(n--) {
    	string str; 
        getline(cin,str);
    	stringstream ss(str);
    	string word, temp = "";
    	while(ss >> word) temp.push_back(word[0]);
		to_lower(temp);
		if(strcmp(temp.data(),s.data()) == 0) {
            mp.insert(pair<string,int>(str, 1));
        }
	}
	cout << mp.size() << endl;
	for(auto x:mp) cout << x.first << endl;
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

