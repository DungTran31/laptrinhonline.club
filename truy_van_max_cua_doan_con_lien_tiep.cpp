void runtime(){
    #ifndef ONLINE_JUDGE
        cerr << "\n>> Runtime: " << (double)clock() / CLOCKS_PER_SEC << "s\n";
    #endif
}

struct Node {
    int a, b, elem;
    Node *left, *right;
    Node(int a, int b) : a(a), b(b), elem(-1000000000), left(nullptr), right(nullptr) {
        if (a + 1 == b) {
            left = nullptr;
            right = nullptr;
        } else {
            left = new Node(a, (a + b) / 2);
            right = new Node((a + b) / 2, b);
        }
    }
};

void update(Node* H, int i, int x) {
    if (H->elem < x) {
        H->elem = x;
    }
    if (H->left != nullptr) {
        update((i < H->left->b) ? H->left : H->right, i, x);
    }
}

int get(Node* H, int L, int R) {
    if (H->a == L && H->b == R) {
        return H->elem;
    }
    if (R <= H->left->b) {
        return get(H->left, L, R);
    }
    if (L >= H->right->a) {
        return get(H->right, L, R);
    }
    return max(get(H->left, L, H->left->b), get(H->right, H->right->a, R));
}

void Print(Node* H, string p = "") {
    if (H == nullptr) {
        return;
    }
    Print(H->left, p + "\t\t");
    cout << p << "(" << H->a << "," << H->b << "):" << H->elem << endl;
    Print(H->right, p + "\t\t");
}

void sol() {
    int n, m;
    cin >> n >> m;
    Node* root = new Node(1, n + 1);
    for (int i = 1; i <= n; ++i) {
        int x;
        cin >> x;
        update(root, i, x);
    }
    for (int i = 0; i < m; ++i) {
        int L, R;
        cin >> L >> R;
        cout << get(root, L, R + 1) << endl;
    }
}

main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    sol();

    runtime();
    return 0;
}

